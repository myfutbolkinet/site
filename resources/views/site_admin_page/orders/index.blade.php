@section('scripts')

    <script>
        reloadData();
        function reloadData(){

            var module='admin.main_admin.orders.data'
            var url='/admin/orders/data';
            $.ajax({
                method: 'POST',
                dataType: 'html',
                async:false,
                url: url,
                data: {module: module},
                beforeSend: function() {
                    $('#loader').show();
                },
                complete: function() {
                    $('#loader').hide();
                },
                success: function (data) {

                    $('#tableGrid').html(data);

                }
            });


        }


</script>
